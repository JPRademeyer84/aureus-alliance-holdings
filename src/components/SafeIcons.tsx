import React from 'react';
import { cn } from '@/lib/utils';

// Safe icon components that replace Lucide React icons with emoji fallbacks
// This prevents the "className.includes is not a function" error

interface SafeIconProps {
  className?: string | undefined | null;
  size?: number;
  [key: string]: any;
}

const createSafeIcon = (emoji: string, name: string) => {
  const SafeIcon: React.FC<SafeIconProps> = ({ className, size = 16, ...props }) => {
    const safeClassName = typeof className === 'string' ? className : '';
    return (
      <span 
        className={cn("inline-block", safeClassName)}
        style={{ 
          fontSize: `${size}px`,
          lineHeight: 1,
          display: 'inline-block',
          verticalAlign: 'middle'
        }}
        role="img"
        aria-label={name}
        {...props}
      >
        {emoji}
      </span>
    );
  };
  SafeIcon.displayName = `Safe${name}Icon`;
  return SafeIcon;
};

